					<h1><b>Política de cookies</b></h1><br>
					<h3>¿Qué es una cookie?</h3><br>
					<p>Una cookie es un pequeño archivo que se almacena en tu navegador, para solo funcionar con la página web de donde provienen.</p>
					<p>Estos sirven para hacer la página mas responsiva y que sea más facíl de navegar por ella, también se utilizan para almacenar cuentas de usuario en el caso de que quieras que se te recuerde</p>
					<p>Hay varios tipos de cookies:</p>
					<ul>
						<li>Cookies analíticas.- Las cookies analíticas recogen información para evaluar el uso que usted hace de la Web y la actividad general de la misma. La analítica de la Web es la medida, recolección, análisis y creación de informes de los datos de Internet con el propósito de entender y optimizar el uso de sitio Web.</li>
						<li>Cookies sociales.- Las cookies sociales son necesarias para las redes sociales externas (Facebook, Google, Twitter, Pinteres,Discus, etc…). Su función es controlar la interacción con los widgets sociales dentro de la página.</li>
						<li>Cookies de afiliados.- Las cookies de afiliados permiten hacer un seguimiento de las visitas procedentes de otras Webs, con las que nuestra web tiene un contrato de afiliación.</li>
						<li>Cookies de publicidad y comportamentales.- Las cookies de publicidad y comportamentales recogen información sobre sus preferencias y elecciones personales en este sitio Web. Permiten adecuar el contenido de la publicidad para que esta sea relevante para el usuario y para evitar mostrar anuncios que el usuario ya haya visto.</li>
						<li>Cookies técnicas.- Las cookies técnicas son las estrictamente necesarias para el uso de este sitio Web, permiten al usuario la navegación a través de la página Web o aplicación y la utilización de las diferentes opciones o servicios que en ella existen.</li>
						<li>Cookies de personalización.- Son aquéllas que permiten al usuario acceder al servicio con algunas características de carácter general predefinidas en su terminal o que el propio usuario defina. Por ejemplo, el idioma, el tipo de navegador a través del cual accede al servicio, el diseño de contenidos seleccionado, geolocalización del terminal y la configuración regional desde donde se accede al servicio.</li>
						<li>Cookies de terceros.- En algunas páginas Web se pueden instalar cookies de terceros que permiten gestionar y mejorar los servicios ofrecidos.</li>
					</ul>

